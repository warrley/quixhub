export function disciplineById(id: string) {
  return disciplines.find((d) => d.id === id);
}

export function materialsByDiscipline(id: string) {
  return materials.filter((m) => m.disciplineId === id && m.status === 'published');
}

export function pendingMaterials() {
  return materials.filter((m) => m.status === 'pending');
}
